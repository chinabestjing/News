<script type="text/javascript">
        var bus = $(".bus-page1"),
                pics_s = bus.find(".pics"),
                pics = pics_s.children(),
                curr_p = pics.eq(0).stop().fadeIn(300),
        // 下标
                idxs_s = bus.find(".idxs"),
                idxs = idxs_s.children(),
                curr_i = idxs.eq(0).addClass("on"),
        // 记录
                len = pics.length,
                idx = 0,
                idx_m = len - 1,
                prev_i = -1,
        // 自动切换
                tt = 0,
                delay = 5000;

        var conts = $('.sContP'),
                curr_c = conts.eq(0).show(),
                tags   = $('#tags a'),
                curr_t = tags.eq(0).addClass('on');

        curr_c.siblings().hide();
        // 标签点击切换
        tags.on('click', function(tag, idx) {
            curr_t.removeClass('on');
            curr_t = $(this).addClass('on');
            idx = curr_t.index();
            curr_c.hide();
            curr_c = conts.eq(idx).show();
            fade(idx);
        });

        win.on('load', function () {
            // 使下标点屏幕居中
            idxs_s.css('margin-left', -(idxs_s.width() - parseInt(curr_i.css('margin-right'))));
            // 下标点出现
            idxs_slideUp(0, len);
            // 下标点 hover 控制 初始化
            idxs.click(function () {
                fade($(this).index());
            });
            // 自动切换
            auto();
            // 默认高亮第一个子tag
            $('.wMenuTag ul').each(function() {
                if (window.console) {
                    console.log($(this).children().eq(0));
                }
                $(this).children().eq(0).addClass('cur');
            });
        });


        function auto() {
            tt = setTimeout(function () {
                fade(idx === idx_m ? idx = 0 : ++idx);
            }, delay);
        }

        // 切换函数
        function fade(i) {
            if (i === prev_i) return;
            clearTimeout(tt);
            prev_i = i;
            curr_p.stop(true, true).fadeOut(300);
            curr_p = pics.eq(i).stop(true, true).fadeIn(300);
            curr_i.removeClass("on");
            curr_i = idxs.eq(i).addClass("on");
            auto();
        }

        // 下标出现动画
        function idxs_slideUp(i, len) {
            idxs.eq(i).animate({ "margin-top": 0 }, { "duration": 500, "easing": "easeInOutBack" });
            if (i < len) {
                setTimeout(function () {
                    idxs_slideUp(++i, len);
                }, 300);
            }
        }

</script>

